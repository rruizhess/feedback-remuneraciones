    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String clvactual=this.txtclvactual.getText();
        String clave1=this.txtnewclv.getText();
        String Clave2=this.txtverclave.getText();
        if(!clave1.equals(Clave2)){
            JOptionPane.showMessageDialog(this, "Las claves ingresadas no coinciden.");
        }else{
            String user=rutid;
            String pass=clvactual;
            Usuario u=new Usuario();
            u=u.verificarUsuario(user, pass);
            if(u==null){
            JOptionPane.showMessageDialog(this, "La contraseña no es valida.");
            }else{
                control ctrl=new control();
                ctrl.updateClave(clave1, user);
                JOptionPane.showMessageDialog(this, "Se ha cambiado su contraseña exitosamente.", "Resultado cambio de contraseña...", WIDTH);
                this.erase();
                dispose();
                try {
                    l.ad("Cambio de contraseña: El usuario id:["+user+"] ha cambiado su contraseña");
                } catch (IOException ex) {
                    Logger.getLogger(editarclave.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

